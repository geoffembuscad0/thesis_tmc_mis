		<h1 id='ourStory' style="text-align:right;">Our Story</h1>
		<div class='scrollStory' style="overflow: hidden; height:165px;">
		<p>
		Today, Teresa Marble Corporation is the preeminent and acknowledged leader in the Philippine marble industry. The Rizal quarry site alone with its 7-hectare marble processing plant has an export capacity of 20,000 square meter slabs per month and 10,000 square meters of tiles per month.
The STORY of Teresa Marble Corporation started in the mid-seventies when Pasig Mining Corporation headed by Mr. Francisco Dee perfected mining rights over a marble mountain claim located in Antipolo, Rizal. At that time, Mr. Francisco Dee did not possess the necessary skills and knowledge in the field of mining as a business.
A younger sister, Annie, encouraged by their mother, Esperanza, conducted a study. The result yielded the possibility of extracting and mining non-metallic components from the marble claim. This eventually paved the way for the establishment of Teresa marble Corporation in 1976.
The company started its very first operation with just one air compressor and one contractor quarrying small marble blocks Just one year after its establishment, the demand for the company’s product prompted them to increase its contractor operations to three.
At that time, the company had already begun quarry operations for boulders too. Still, operations were mostly manual in nature with the use of air compressors and improvised block lifters.
By 1981 Teresa Marble Corporation had already built its own marble processing plant. The installation of blokita tiling machine(s) enabled the company to produce 30 x 60 cm tiles. Simultaneously, the company decided to hire full-time employees who were skilled in marble processing machine operations and set-ups.
Gradually, as operations because more complex, better machines such as Derrick crane, bock cutter, S4C, and polisher were procured. Large marble blocks, at that time, were still sold to Philippine marble processing companies. It was not until 1985, that Teresa Marble Corporation bought and set-up its first Gang Saw which enabled the cutting of large marble blocks.
In 1986, as the company’s business operations continued to grow, professional engineers were hired as full-time staff in order to oversee and ensure the smooth flow of various quarrying and processing operations. It was also this same year when the company expanded its quarry operations to Cebu.
By 1993, Teresa Marble Corporation continued to grow in product excellence and skills. As volume demands and orders increased, the company initiated a major technological upgrade by shifting its marble quarry production technology from drilling to a new engine-driven Wire Saw cutting technology. This new technology greatly improved the quality and quantity of marble blocks produced by the company. Two years later, a more advanced Electric Wire Saw technology was introduced.
At the same time, expert technical staffs were commissioned and sent by the company to Europe and Japan for further skills and training enhancements. Key marketing and sales personnel likewise visited different parts of the world to study and gain further knowledge in marble as a valued product.
Since then, Teresa Marble Corporation has become a formidable player in the marble industry in the Philippines. Significant capital investments brought the overall competitiveness of the company to new heights . Purchase of heavy equipment such as backhoe, pay loader, crane and many others propelled the production capacity even further.
By this time, with ample experience, necessary skills and technologies, the company was able to gradually expand its product lines, from raw marble to processed tiles, slabs, decors and novelty items. Full-time artisans and craftsmen utilized high-end European machines to produce fine architectural components that can be used in constructions finishing and decorative applications.
Product & process research and development strategy institutionalized by the company, contributes to an evolving array of product service offerings for stone users. This together with continuing focus and investment on productivity and training maintain the overall competitiveness of the company on a world scale.
Today, Teresa Marble Corporation is the preeminent and acknowledge leader in the Philippine marble industry. The Rizal quarry site alone with its 7-hectare marble processing plant has an export capacity of 20,000 square meters of slabs and 10,000 square meters of tiles per month.
Teresa Marble Corporation continues to transform and touch the lives of its employees; create a sustainable environment in the communities it operates in; supplies quality marble products for global living.
